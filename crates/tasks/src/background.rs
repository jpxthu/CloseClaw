// TaskManager — bridge to closeclaw_common trait
// ═══════════════════════════════════════════════════════════════════════════

#[async_trait::async_trait]
impl crate::TaskManager for BackgroundTaskManager {
    async fn spawn_task(
        &self,
        command: &str,
        cwd: &std::path::Path,
    ) -> Result<BackgroundTask, BackgroundTaskError> {
        self.spawn(command, cwd).await
    }

    async fn backgroundize_task(
        &self,
        child: tokio::process::Child,
        command: &str,
    ) -> Result<BackgroundTask, BackgroundTaskError> {
        self.backgroundize(child, command).await
    }

    async fn kill_task(&self, task_id: &str) -> Result<(), BackgroundTaskError> {
        self.kill(task_id).await
    }

    async fn get_task(&self, task_id: &str) -> Option<BackgroundTask> {
        self.get_task(task_id).await
    }

    async fn drain_notifications(&self) -> Vec<CompletionNotification> {
        self.pending_notifications().await
    }

    async fn cleanup_finished(&self) {
        self.cleanup_finished().await
    }
}

// ---------------------------------------------------------------------------
// Process execution helpers
// ---------------------------------------------------------------------------

async fn run_shell_command(
    command: &str,
    cwd: &Path,
    output_path: &Path,
    tasks: &TaskMap,
    task_id: &str,
    notifications: &Arc<Mutex<Vec<CompletionNotification>>>,
) {
    let result = Command::new("sh")
        .arg("-c")
        .arg(command)
        .current_dir(cwd)
        .stdout(std::process::Stdio::piped())
        .stderr(std::process::Stdio::piped())
        .spawn();

    match result {
        Ok(child) => {
            run_background_child(child, output_path, tasks, task_id, notifications).await;
        }
        Err(e) => {
            mark_task_failed(tasks, task_id, &e).await;
        }
    }
}

async fn run_background_child(
    mut child: tokio::process::Child,
    output_path: &Path,
    tasks: &TaskMap,
    task_id: &str,
    notifications: &Arc<Mutex<Vec<CompletionNotification>>>,
) {
    let stdout = child.stdout.take();
    let stderr = child.stderr.take();
    let (kill_tx, kill_rx) = oneshot::channel();

    {
        let mut map = lock_map(tasks).await;
        if let Some(h) = map.get_mut(task_id) {
            h.kill_tx = Some(kill_tx);
        }
    }

    let exit_code = await_process(child, stdout, stderr, output_path, kill_rx).await;

    finalize_state(tasks, task_id, exit_code, notifications).await;
}

async fn mark_task_failed(tasks: &TaskMap, task_id: &str, error: &std::io::Error) {
    let mut map = lock_map(tasks).await;
    if let Some(h) = map.get_mut(task_id) {
        h.state = TaskState::Failed { exit_code: 1 };
        tracing::error!(
            task_id = %task_id,
            error = %error,
            "failed to spawn background command"
        );
    }
}

async fn backgroundize_process(
    child: tokio::process::Child,
    stdout: Option<tokio::process::ChildStdout>,
    stderr: Option<tokio::process::ChildStderr>,
    output_path: &Path,
    tasks: &TaskMap,
    task_id: &str,
    notifications: &Arc<Mutex<Vec<CompletionNotification>>>,
) {
    let (kill_tx, kill_rx) = oneshot::channel();
    {
        let mut map = lock_map(tasks).await;
        if let Some(h) = map.get_mut(task_id) {
            h.kill_tx = Some(kill_tx);
        }
    }

    let exit_code = await_process(child, stdout, stderr, output_path, kill_rx).await;

    finalize_state(tasks, task_id, exit_code, notifications).await;
}

async fn await_process(
    mut child: tokio::process::Child,
    stdout: Option<tokio::process::ChildStdout>,
    stderr: Option<tokio::process::ChildStderr>,
    output_path: &Path,
    kill_rx: oneshot::Receiver<()>,
) -> i32 {
    let file = tokio::fs::OpenOptions::new()
        .create(true)
        .append(true)
        .open(output_path)
        .await;

    let file = match file {
        Ok(f) => f,
        Err(e) => {
            tracing::error!(
                task_id = "process",
                error = %e,
                "failed to open output file"
            );
            return 1;
        }
    };

    let stdout_file = file.try_clone().await.expect("cloning output file handle");
    let stdout_handle = tokio::spawn(copy_reader(stdout, stdout_file));
    let stderr_handle = tokio::spawn(copy_reader(stderr, file));

    let code = tokio::select! {
        result = child.wait() => {
            result.map(|s| s.code().unwrap_or(1)).unwrap_or(1)
        }
        _ = kill_rx => {
            let _ = child.kill().await;
            -1
        }
    };

    let _ = stdout_handle.await;
    let _ = stderr_handle.await;
    code
}

async fn copy_reader<R: tokio::io::AsyncRead + Unpin>(
    mut reader: Option<R>,
    mut file: tokio::fs::File,
) {
    if let Some(ref mut r) = reader {
        let _ = tokio::io::copy(r, &mut file).await;
    }
}

async fn finalize_state(
    tasks: &TaskMap,
    task_id: &str,
    exit_code: i32,
    notifications: &Arc<Mutex<Vec<CompletionNotification>>>,
) {
    let mut map = lock_map(tasks).await;
    if let Some(h) = map.get_mut(task_id) {
        // Do not overwrite an explicit Kill.
        if h.state == TaskState::Killed {
            return;
        }
        let new_state = if exit_code == 0 {
            TaskState::Completed { exit_code }
        } else {
            TaskState::Failed { exit_code }
        };
        h.state = new_state.clone();
        // Dedup: if a stuck alert was already sent, skip completion
        // notification — the task only notifies once.
        if h.notified {
            return;
        }
        h.notified = true;
        let summary = format!("Background command '{}' completed", h.command);
        let notif = CompletionNotification {
            task_id: h.id.clone(),
            command: h.command.clone(),
            state: new_state,
            output_path: h.output_path.clone(),
            priority: NotificationPriority::Later,
            summary,
        };
        notifications.lock().await.push(notif);
    }
}

// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

async fn lock_map(tasks: &TaskMap) -> tokio::sync::MutexGuard<'_, HashMap<String, TaskHandle>> {
    tasks.lock().await
}

async fn prepare_task_dir(temp_dir: &Path, task_id: &str) -> Result<PathBuf, BackgroundTaskError> {
    let dir = temp_dir.join("closeclaw/background").join(task_id);

    tokio::fs::create_dir_all(&dir)
        .await
        .map_err(BackgroundTaskError::Io)?;

    Ok(dir.join("output"))
}

async fn insert_initial_handle(tasks: &TaskMap, task_id: &str, command: &str, output_path: &Path) {
    let handle = TaskHandle {
        id: task_id.to_owned(),
        command: command.to_owned(),
        state: TaskState::Running,
        output_path: output_path.to_path_buf(),
        kill_tx: None,
        notified: false,
    };
    let mut map = lock_map(tasks).await;
    map.insert(task_id.to_owned(), handle);
}

fn make_public_task(task_id: &str, command: &str, output_path: &Path) -> BackgroundTask {
    BackgroundTask {
        id: task_id.to_owned(),
        command: command.to_owned(),
        state: TaskState::Running,
        output_path: output_path.to_path_buf(),
    }
}

