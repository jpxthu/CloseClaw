//! Background task management for async command execution.
//! Manages background process lifecycle, output, and stuck detection.

use std::collections::HashMap;
use std::path::{Path, PathBuf};
use std::sync::Arc;

use serde::{Deserialize, Serialize};
use thiserror::Error;
use tokio::process::Command;
use tokio::sync::{oneshot, Mutex};
use uuid::Uuid;

use crate::stuck_detect::{self, StuckDetectConfig};

pub(crate) type TaskMap = Arc<Mutex<HashMap<String, TaskHandle>>>;

/// Errors from the background task subsystem.
#[derive(Debug, Error)]
pub enum BackgroundTaskError {
    #[error("spawn failed: {0}")]
    SpawnFailed(String),
    #[error("task not found: {0}")]
    NotFound(String),
    #[error("task not running: {0}")]
    NotRunning(String),
    #[error("io error: {0}")]
    Io(#[from] std::io::Error),
}

/// Lifecycle state of a background task.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum TaskState {
    /// Process is running.
    Running,
    /// Process exited successfully.
    Completed { exit_code: i32 },
    /// Process exited with a non-zero exit code.
    Failed { exit_code: i32 },
    /// Process was killed externally.
    Killed,
}

/// A handle to a background task.
#[derive(Debug, Clone)]
pub struct BackgroundTask {
    pub id: String,
    pub command: String,
    pub state: TaskState,
    pub output_path: PathBuf,
}

/// Notification delivery priority.
///
/// Variant declaration order determines the derived `Ord` ordering:
/// `Now > Next > Later`.
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Serialize, Deserialize)]
pub enum NotificationPriority {
    /// Inject at a convenient later point.
    Later,
    /// Inject into the next dialogue turn immediately.
    Next,
    /// Highest priority — inject immediately (before user input).
    /// Used for system-level urgent notifications.
    Now,
}

/// A pending completion notification generated by the manager.
#[derive(Debug, Clone)]
pub struct CompletionNotification {
    /// Identifier of the completed task.
    pub task_id: String,
    /// Original command.
    pub command: String,
    /// Final task state.
    pub state: TaskState,
    /// Path to the task output file.
    pub output_path: PathBuf,
    /// Delivery priority.
    pub priority: NotificationPriority,
    /// Human-readable summary for the notification.
    pub summary: String,
}

/// Internal handle for per-task bookkeeping.
pub(crate) struct TaskHandle {
    pub(crate) id: String,
    pub(crate) command: String,
    pub(crate) state: TaskState,
    pub(crate) output_path: PathBuf,
    pub(crate) kill_tx: Option<oneshot::Sender<()>>,
    pub(crate) notified: bool,
}

// ---------------------------------------------------------------------------
// Manager
// ---------------------------------------------------------------------------

/// Manages the full lifecycle of background tasks.
pub struct BackgroundTaskManager {
    tasks: TaskMap,
    temp_dir: PathBuf,
    notifications: Arc<Mutex<Vec<CompletionNotification>>>,
}

impl Default for BackgroundTaskManager {
    fn default() -> Self {
        Self::new()
    }
}

impl BackgroundTaskManager {
    /// Create a new manager using the default temp root (`/tmp`).
    pub fn new() -> Self {
        Self::with_temp_dir("/tmp")
    }

    /// Create a new manager with an explicit temp root directory.
    pub fn with_temp_dir(temp_dir: impl Into<PathBuf>) -> Self {
        Self {
            tasks: Arc::new(Mutex::new(HashMap::new())),
            temp_dir: temp_dir.into(),
            notifications: Arc::new(Mutex::new(Vec::new())),
        }
    }
}

impl BackgroundTaskManager {
    /// Spawn a shell command in the background, returning immediately.
    pub async fn spawn(
        &self,
        command: &str,
        cwd: &Path,
    ) -> Result<BackgroundTask, BackgroundTaskError> {
        let task_id = Uuid::new_v4().to_string();
        let output_path = prepare_task_dir(&self.temp_dir, &task_id).await?;

        insert_initial_handle(&self.tasks, &task_id, command, &output_path).await;

        stuck_detect::start_stuck_detection(
            task_id.clone(),
            output_path.clone(),
            command.to_owned(),
            Arc::clone(&self.tasks),
            Arc::clone(&self.notifications),
            StuckDetectConfig::default(),
        );

        let shared = Arc::clone(&self.tasks);
        let cmd = command.to_owned();
        let cwd = cwd.to_path_buf();
        let out = output_path.clone();
        let tid = task_id.clone();

        let notifs = Arc::clone(&self.notifications);

        tokio::spawn(async move {
            run_shell_command(&cmd, &cwd, &out, &shared, &tid, &notifs).await;
        });

        Ok(make_public_task(&task_id, command, &output_path))
    }

    /// Take over a running child process.
    pub async fn backgroundize(
        &self,
        mut child: tokio::process::Child,
        command: &str,
    ) -> Result<BackgroundTask, BackgroundTaskError> {
        let task_id = Uuid::new_v4().to_string();
        let output_path = prepare_task_dir(&self.temp_dir, &task_id).await?;

        let stdout = child.stdout.take();
        let stderr = child.stderr.take();

        insert_initial_handle(&self.tasks, &task_id, command, &output_path).await;

        stuck_detect::start_stuck_detection(
            task_id.clone(),
            output_path.clone(),
            command.to_owned(),
            Arc::clone(&self.tasks),
            Arc::clone(&self.notifications),
            StuckDetectConfig::default(),
        );

        let shared = Arc::clone(&self.tasks);
        let out = output_path.clone();
        let tid = task_id.clone();

        let notifs = Arc::clone(&self.notifications);

        tokio::spawn(async move {
            backgroundize_process(child, stdout, stderr, &out, &shared, &tid, &notifs).await;
        });

        Ok(make_public_task(&task_id, command, &output_path))
    }
}

impl BackgroundTaskManager {
    /// Send SIGKILL to a running task, transitioning to Killed.
    pub async fn kill(&self, task_id: &str) -> Result<(), BackgroundTaskError> {
        let mut map = lock_map(&self.tasks).await;
        let handle = map
            .get_mut(task_id)
            .ok_or_else(|| BackgroundTaskError::NotFound(task_id.to_owned()))?;

        if handle.state != TaskState::Running {
            return Err(BackgroundTaskError::NotRunning(task_id.to_owned()));
        }

        if let Some(kill_tx) = handle.kill_tx.take() {
            let _ = kill_tx.send(());
        }
        handle.state = TaskState::Killed;
        Ok(())
    }

    /// Return `true` if the task is in the [`TaskState::Running`] state.
    pub async fn is_running(&self, task_id: &str) -> bool {
        let map = lock_map(&self.tasks).await;
        map.get(task_id)
            .is_some_and(|h| h.state == TaskState::Running)
    }

    /// Retrieve a snapshot of the current task state.
    pub async fn get_task(&self, task_id: &str) -> Option<BackgroundTask> {
        let map = lock_map(&self.tasks).await;
        map.get(task_id).map(|h| BackgroundTask {
            id: h.id.clone(),
            command: h.command.clone(),
            state: h.state.clone(),
            output_path: h.output_path.clone(),
        })
    }

    /// Mark a task as notified (dedup support for Step 1.3).
    pub async fn mark_notified(&self, task_id: &str) {
        let mut map = lock_map(&self.tasks).await;
        if let Some(h) = map.get_mut(task_id) {
            h.notified = true;
        }
    }

    /// Drain pending notifications without re-delivery.
    pub async fn pending_notifications(&self) -> Vec<CompletionNotification> {
        let mut n = self.notifications.lock().await;
        std::mem::take(&mut *n)
    }

    /// Remove output directories and handles for finished tasks.
    ///
    /// - [`TaskState::Completed`] and [`TaskState::Failed`]: output
    ///   directory is removed and the handle is evicted from the map.
    /// - [`TaskState::Killed`]: the handle is evicted but the output
    ///   directory is preserved so the caller can inspect what was
    ///   written before the kill.
    ///
    /// I/O errors are logged but do not propagate — this method never
    /// panics.
    pub async fn cleanup_finished(&self) {
        let mut map = lock_map(&self.tasks).await;
        let finished: Vec<String> = map
            .iter()
            .filter(|(_, h)| {
                matches!(
                    h.state,
                    TaskState::Completed { .. } | TaskState::Failed { .. } | TaskState::Killed
                )
            })
            .map(|(id, _)| id.clone())
            .collect();

        for task_id in &finished {
            if let Some(handle) = map.get(task_id) {
                // Preserve output directories for killed tasks — the caller
                // should be able to inspect what was written before the kill.
                if handle.state != TaskState::Killed {
                    let parent = handle.output_path.parent();
                    if let Some(dir) = parent {
                        if let Err(e) = tokio::fs::remove_dir_all(dir).await {
                            tracing::warn!(
                                task_id = %task_id,
                                path = %dir.display(),
                                error = %e,
                                "failed to remove task output directory"
                            );
                        }
                    }
                }
            }
            map.remove(task_id);
        }
    }
}

